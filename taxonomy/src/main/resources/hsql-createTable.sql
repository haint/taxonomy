DROP TABLE IF EXISTS Kingdom CASCADE
CREATE CACHED TABLE Kingdom (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	CODE VARCHAR(255) UNIQUE NOT NULL,
	NAME VARCHAR(255) NOT NULL,
)

DROP TABLE IF EXISTS Index
CREATE CACHED TABLE Index (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	VALUE VARCHAR(255) UNIQUE NOT NULL
)

DROP TABLE IF EXISTS Tag
CREATE CACHED TABLE Tag (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	NAME VARCHAR(255) UNIQUE NOT NULL,
	EXPLAINTION LONGVARCHAR
)

DROP TABLE IF EXISTS Locales
CREATE CACHED TABLE Locales (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	NAME VARCHAR(255) NOT NULL,
	VALUE VARCHAR(255)
)

DROP TABLE IF EXISTS Family
CREATE CACHED TABLE Family (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	KINGDOM_ID INTEGER NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	LOCALE_IDS VARCHAR(255),
	DESCRIPTION LONGVARCHAR,
	AVATAR VARCHAR(255),
	FOREIGN KEY (KINGDOM_ID) REFERENCES KINGDOM(ID)
)

DROP TABLE IF EXISTS Genus
CREATE CACHED TABLE Genus (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	KINGDOM_ID INTEGER NOT NULL,
	NAME VARCHAR(255) NOT NULL,
	LOCALE_IDS VARCHAR(255),
	DESCRIPTION LONGVARCHAR,
	AVATAR VARCHAR(255),
	VARIANT_IDS VARCHAR(255),
	FOREIGN KEY(KINGDOM_ID) REFERENCES KINGDOM(ID)
)

DROP TABLE IF EXISTS Species
CREATE CACHED TABLE Species (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	NAME VARCHAR(255) NOT NULL,
	VARIANT_IDS VARCHAR(255)
)

DROP TABLE IF EXISTS Glossary
CREATE CACHED TABLE Glossary (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	NAME VARCHAR(255) NOT NULL,
	LOCALE_IDS VARCHAR(255),
	EXPLAINTION LONGVARCHAR,
	EXAMPLE LONGVARCHAR
)

DROP TABLE IF EXISTS Variant
CREATE CACHED TABLE Variant (
	ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
	VALUE VARCHAR(255),
	TYPE INTEGER	
)

/* EN_NAMES and VN_NAMES contain multi value and separate by symbol '::' */
DROP TABLE IF EXISTS NaturalObject
CREATE CACHED TABLE NaturalObject
(
   ID INTEGER GENERATED BY DEFAULT AS IDENTITY (START WITH 1) PRIMARY KEY,
   KINGDOM_ID INTEGER NOT NULL,
   FAMILY_IDS VARCHAR(255) NOT NULL,
   INDEX_IDS VARCHAR(255),
   GENUS_ID INTEGER NOT NULL,
   SPECIES_ID INTEGER NOT NULL,
   EN_NAMES LONGVARCHAR,
   VN_NAMES LONGVARCHAR,
   TAG_IDS VARCHAR(255),
   CREATE_DATE VARCHAR(255),
   MODIFIY_DATE VARCHAR(255),
   REFERENCE VARCHAR(255),
   DES LONGVARCHAR,
   AVATAR VARCHAR(255),
   FOREIGN KEY (KINGDOM_ID) REFERENCES KINGDOM(ID)
)